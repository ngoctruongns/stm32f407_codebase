void TIM4_enable_PWM(uint32_t Channel) {
    LL_TIM_CC_EnableChannel(TIM4, Channel);
}

void TIM4_disable_PWM(uint32_t Channel) {
    LL_TIM_CC_DisableChannel(TIM4, Channel);
}

// Timer4 set PWM duty cycle
void TIM4_Set_PWM_DutyCycle(uint32_t Channel, uint32_t DutyCycle) {
    // Assuming DutyCycle is in microseconds for servo control
    // Convert microseconds to timer counts
    // Timer clock = 1 MHz (1 count = 1 microsecond)
    switch (Channel) {
        case LL_TIM_CHANNEL_CH1:
            LL_TIM_OC_SetCompareCH1(TIM4, DutyCycle);
            break;
        case LL_TIM_CHANNEL_CH2:
            LL_TIM_OC_SetCompareCH2(TIM4, DutyCycle);
            break;
        case LL_TIM_CHANNEL_CH3:
            break;
        default:
            return; // Invalid channel
    }

}

// Config servo motor
void Servo_Init(void) {
    // Enable PWM on the specified channel
    for (int i = 0; i < SERVO_MAX_ID; i++) {
        Servo_Config* config = servo_configs[i];
        TIM4_enable_PWM(config->timer_channel);
    }
}

void Servo_setAngle(Servo_ID servo_id, int16_t angle) {
    if (servo_id >= SERVO_MAX_ID) {
        return; // Invalid servo ID
    }

    Servo_Config* config = servo_configs[servo_id];

    // Clamp angle to min and max
    if (angle < config->min_angle) {
        angle = config->min_angle;
    } else if (angle > config->max_angle) {
        angle = config->max_angle;
    }

    // Map angle to pulse width
    uint32_t pulse_width = config->min_pulse +
        ((config->max_pulse - config->min_pulse) * (angle - config->min_angle)) /
        (config->max_angle - config->min_angle);

    // Set PWM duty cycle (pulse width in microseconds)
    TIM4_Set_PWM_DutyCycle(config->timer_channel, pulse_width);
}

